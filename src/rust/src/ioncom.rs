// --- aprok 2D Interpolator ---

fn aprok(
    j1m: &[i32; 7],
    j2m: &[i32; 7],
    h1: &[[f32; 13]; 7],
    h2: &[[f32; 13]; 7],
    r1m: &[[f32; 13]; 7],
    r2m: &[[f32; 13]; 7],
    rk1m: &[[f32; 13]; 7],
    rk2m: &[[f32; 13]; 7],
    hei: f32,
    xhi: f32,
) -> (f32, f32) {
    let zm = [20.0, 40.0, 60.0, 70.0, 80.0, 85.0, 90.0];
    let h = hei;
    let z = xhi;

    let mut j1 = 1;
    let mut i1 = 0;

    for i in 0..7 {
        i1 = i;
        if (z - zm[i]).abs() < 1e-7 {
            j1 = 0;
        }
        if z <= zm[i] {
            break;
        }
    }

    let eval_at_idx = |idx: usize| -> (f32, f32) {
        let mut i2 = 0;
        let limit1 = j1m[idx] as usize;
        for i in 1..limit1 {
            i2 = i - 1;
            if h < h1[idx][i] {
                break;
            }
            i2 = limit1 - 1;
        }

        let mut i3 = 0;
        let limit2 = j2m[idx] as usize;
        for i in 1..limit2 {
            i3 = i - 1;
            if h < h2[idx][i] {
                break;
            }
            i3 = limit2 - 1;
        }

        let r01 = r1m[idx][i2];
        let r02 = r2m[idx][i3];
        let rk1 = rk1m[idx][i2];
        let rk2 = rk2m[idx][i3];
        let h01 = h1[idx][i2];
        let h02 = h2[idx][i3];

        let r1 = r01 + rk1 * (h - h01);
        let r2 = r02 + rk2 * (h - h02);
        (r1, r2)
    };

    if j1 == 0 {
        eval_at_idx(i1)
    } else {
        let (r11, r12) = eval_at_idx(i1);
        let (r1, r2) = eval_at_idx(i1 - 1);
        let rk = (z - zm[i1 - 1]) / (zm[i1] - zm[i1 - 1]);
        let final_r1 = r1 + (r11 - r1) * rk;
        let final_r2 = r2 + (r12 - r2) * rk;
        (final_r1, final_r2)
    }
}

// --- Danilov-Smirnova DS-95 Molecular Ion Model ---

pub fn ionco2(hei: f32, xhi: f32, it: i32, f: f32) -> [f32; 4] {
    let mut z = xhi;
    if z < 20.0 {
        z = 20.0;
    }
    if z > 90.0 {
        z = 90.0;
    }

    let mut r170 = 0.0;
    let mut r270 = 0.0;
    let mut r1140 = 0.0;
    let mut r2140 = 0.0;
    let mut r1 = 0.0;
    let mut r2 = 0.0;

    let is_winter = it == 1 || it == 2 || it == 11 || it == 12;
    let is_summer = it == 5 || it == 6 || it == 7 || it == 8;
    let is_equinox = it == 3 || it == 4 || it == 9 || it == 10;

    if is_winter {
        if f < 140.0 {
            let (v1, v2) = aprok(&J1MW70, &J2MW70, &H1W70, &H2W70, &R1MW70, &R2MW70, &RK1MW70, &RK2MW70, hei, z);
            r170 = v1;
            r270 = v2;
        }
        if f > 70.0 {
            let (v1, v2) = aprok(&J1MW140, &J2MW140, &H1W140, &H2W140, &R1MW140, &R2MW140, &RK1MW140, &RK2MW140, hei, z);
            r1140 = v1;
            r2140 = v2;
        }
        if f <= 70.0 {
            r1 = r170;
            r2 = r270;
        } else if f >= 140.0 {
            r1 = r1140;
            r2 = r2140;
        } else {
            r1 = r170 + (r1140 - r170) * (f - 70.0) / 70.0;
            r2 = r270 + (r2140 - r270) * (f - 70.0) / 70.0;
        }
    } else if is_summer {
        if f < 140.0 {
            let (v1, v2) = aprok(&J1MS70, &J2MS70, &H1S70, &H2S70, &R1MS70, &R2MS70, &RK1MS70, &RK2MS70, hei, z);
            r170 = v1;
            r270 = v2;
        }
        if f > 70.0 {
            let (v1, v2) = aprok(&J1MS140, &J2MS140, &H1S140, &H2S140, &R1MS140, &R2MS140, &RK1MS140, &RK2MS140, hei, z);
            r1140 = v1;
            r2140 = v2;
        }
        if f <= 70.0 {
            r1 = r170;
            r2 = r270;
        } else if f >= 140.0 {
            r1 = r1140;
            r2 = r2140;
        } else {
            r1 = r170 + (r1140 - r170) * (f - 70.0) / 70.0;
            r2 = r270 + (r2140 - r270) * (f - 70.0) / 70.0;
        }
    } else if is_equinox {
        if f < 140.0 {
            let (v1, v2) = aprok(&J1MR70, &J2MR70, &H1R70, &H2R70, &R1MR70, &R2MR70, &RK1MR70, &RK2MR70, hei, z);
            r170 = v1;
            r270 = v2;
        }
        if f > 70.0 {
            let (v1, v2) = aprok(&J1MR140, &J2MR140, &H1R140, &H2R140, &R1MR140, &R2MR140, &RK1MR140, &RK2MR140, hei, z);
            r1140 = v1;
            r2140 = v2;
        }
        if f <= 70.0 {
            r1 = r170;
            r2 = r270;
        } else if f >= 140.0 {
            r1 = r1140;
            r2 = r2140;
        } else {
            r1 = r170 + (r1140 - r170) * (f - 70.0) / 70.0;
            r2 = r270 + (r2140 - r270) * (f - 70.0) / 70.0;
        }
    }

    let mut r3 = 0.0;
    let mut r4 = 0.0;
    if hei < 100.0 {
        r3 = 100.0 - (r1 + r2);
    } else {
        r4 = 100.0 - (r1 + r2);
    }
    if r3 < 0.0 {
        r3 = 0.0;
    }
    if r4 < 0.0 {
        r4 = 0.0;
    }

    [
        r1.round(),
        r2.round(),
        r3.round(),
        r4.round(),
    ]
}

// --- Danilov-Yaichnikov DY-85 Atomic Ion Model ---

pub fn ionco1(h: f32, zd: f32, fd: f32, fs: f32, t: f32) -> [f32; 4] {
    let z = zd * UMR;
    let f = fd * UMR;

    let mut cm = [0.0; 4];
    let mut hm = [0.0; 4];
    let mut all = [0.0; 4];
    let mut betl = [0.0; 4];
    let mut alh = [0.0; 4];
    let mut beth = [0.0; 4];
    let mut cn = [0.0; 4];

    let cos_z = z.cos();
    let cos_f = f.cos();
    let cos_flux = (0.013 * (300.0 - fs)).cos();
    let cos_season = (0.52 * (t - 6.0)).cos();

    let mut s = 0.0;

    for i in 0..4 {
        let p_species = match i {
            0 => &PO,
            1 => &PH,
            2 => &PN,
            _ => &PHE,
        };

        let mut var = [0.0; 6];
        for j in 0..6 {
            var[j] = p_species[0][j] * cos_z
                + p_species[1][j] * cos_f
                + p_species[2][j] * cos_flux
                + p_species[3][j] * cos_season
                + p_species[4][j];
        }

        cm[i] = var[0];
        hm[i] = var[1];
        all[i] = var[2];
        betl[i] = var[3];
        alh[i] = var[4];
        beth[i] = var[5];

        let hx = h - hm[i];
        if hx < 0.0 {
            let arg = hx * (hx * all[i] + betl[i]);
            if arg > -ARGMAX {
                cn[i] = cm[i] * arg.exp();
            }
        } else if hx == 0.0 {
            cn[i] = cm[i];
        } else {
            let arg = hx * (hx * alh[i] + beth[i]);
            if arg > -ARGMAX {
                cn[i] = cm[i] * arg.exp();
            }
        }

        if cn[i] < 0.005 * cm[i] {
            cn[i] = 0.0;
        }
        if cn[i] > cm[i] {
            cn[i] = cm[i];
        }
        s += cn[i];
    }

    let mut relative_cn = [0.0; 4];
    if s > 0.0 {
        for i in 0..4 {
            relative_cn[i] = cn[i] / s * 100.0;
        }
    }
    relative_cn
}

// --- Danilov Ion Composition Wrapper ---

pub fn iondani(id: i32, ismo: i32, hx: f32, zd: f32, fd: f32, fs: f32) -> [f32; 7] {
    let mut dion = [0.0; 7];
    let h = hx;
    let xhi = zd;
    let xlati = fd;
    let f107 = fs;
    let deci_month = ismo as f32 + (id as f32) / 29.0;

    if h > 300.0 {
        let cn = ionco1(h, xhi, xlati, f107, deci_month);
        dion[0] = cn[0];
        dion[1] = cn[1];
        dion[2] = cn[2];
        dion[3] = cn[3];
        // dion[4..6] are already 0.0
    } else {
        let molecular = ionco2(h, xhi, ismo, f107);
        dion[4] = molecular[0]; // NO+
        dion[5] = molecular[1]; // O2+
        dion[6] = molecular[2]; // Cluster+
        dion[0] = molecular[3]; // O+
    }
    dion
}
