// --- aprok 2D Interpolator ---

fn aprok(
    j1m: &[i32; 7],
    j2m: &[i32; 7],
    h1: &[[f32; 13]; 7],
    h2: &[[f32; 13]; 7],
    r1m: &[[f32; 13]; 7],
    r2m: &[[f32; 13]; 7],
    rk1m: &[[f32; 13]; 7],
    rk2m: &[[f32; 13]; 7],
    hei: f32,
    xhi: f32,
) -> (f32, f32) {
    let zm = [20.0, 40.0, 60.0, 70.0, 80.0, 85.0, 90.0];
    let h = hei;
    let z = xhi;

    let mut j1 = 1;
    let mut i1 = 0;

    for i in 0..7 {
        i1 = i;
        if (z - zm[i]).abs() < 1e-7 {
            j1 = 0;
        }
        if z <= zm[i] {
            break;
        }
    }

    let eval_at_idx = |idx: usize| -> (f32, f32) {
        let mut i2 = 0;
        let limit1 = j1m[idx] as usize;
        for i in 1..limit1 {
            i2 = i - 1;
            if h < h1[idx][i] {
                break;
            }
            i2 = limit1 - 1;
        }

        let mut i3 = 0;
        let limit2 = j2m[idx] as usize;
        for i in 1..limit2 {
            i3 = i - 1;
            if h < h2[idx][i] {
                break;
            }
            i3 = limit2 - 1;
        }

        let r01 = r1m[idx][i2];
        let r02 = r2m[idx][i3];
        let rk1 = rk1m[idx][i2];
        let rk2 = rk2m[idx][i3];
        let h01 = h1[idx][i2];
        let h02 = h2[idx][i3];

        let r1 = r01 + rk1 * (h - h01);
        let r2 = r02 + rk2 * (h - h02);
        (r1, r2)
    };

    if j1 == 0 {
        eval_at_idx(i1)
    } else {
        let (r11, r12) = eval_at_idx(i1);
        let (r1, r2) = eval_at_idx(i1 - 1);
        let rk = (z - zm[i1 - 1]) / (zm[i1] - zm[i1 - 1]);
        let final_r1 = r1 + (r11 - r1) * rk;
        let final_r2 = r2 + (r12 - r2) * rk;
        (final_r1, final_r2)
    }
}

// --- Danilov-Smirnova DS-95 Molecular Ion Model ---

