fn legfun(m: i32, fn_val: f32, rconst: f32, colat: f32) -> (f32, f32, f32) {
    let fnn = (fn_val * (fn_val + 1.0)) as f64;
    let dfarg = std::f64::consts::PI / 180.0;
    let colat_d = colat as f64;
    
    let (x, c) = if colat_d < 60.0 {
        let x_val = (dfarg * colat_d / 2.0).sin().powi(2);
        (x_val, 1.0 - 2.0 * x_val)
    } else {
        let c_val = (colat_d * dfarg).cos();
        ((1.0 - c_val) / 2.0, c_val)
    };
    
    let s = (colat_d * dfarg).sin();
    
    let mut al = if m > 1 {
        (rconst as f64) * s.powi(m - 1)
    } else {
        rconst as f64
    };
    
    let mut pnm = al;
    let mut dpnm = 0.0;
    let mut j = 0;
    let jmax = 60;
    
    loop {
        j += 1;
        let jpm = j + m;
        let b = al * (((jpm - 1) as f64) - fnn / (jpm as f64));
        dpnm += b;
        let a = (b * x) / (j as f64);
        pnm += a;
        al = a;
        
        let abs_a = a.abs();
        let abs_b = b.abs();
        
        if abs_b < 1e-7 && abs_a < 1e-7 {
            break;
        }
        if abs_b >= 1e+13 || abs_a >= 1e+13 || j >= jmax {
            panic!("legfun: numerical error or slow convergence: M={}, FN={}, CONST={}, J={}, A={}, B={}", m, fn_val, rconst, j, a, b);
        }
    }
    
    let ps = pnm;
    let dps = dpnm;
    
    let (p, dp, pms) = if m == 0 {
        (
            ps as f32,
            (dps * s / 2.0) as f32,
            0.0_f32
        )
    } else {
        (
            (ps * s) as f32,
            (dps * s * s / 2.0 + c * ps * (m as f64)) as f32,
            (ps * (m as f64)) as f32
        )
    };
    
    (p, dp, pms)
}

