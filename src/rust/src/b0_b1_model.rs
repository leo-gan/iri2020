fn schnevpd_calc(
    flat: f32,
    flon: f32,
    t: f32,
    bint: &[[[f32; 5]; 7]; 7],
    bext: &[[[f32; 5]; 7]; 7],
    kint: i32,
    lint: i32,
    kext: i32,
    lext: i32,
    kmax: i32,
    fn_arr: &[[f32; 7]; 7],
    constp: &[[f32; 7]; 7],
) -> f32 {
    let tzero = 1.0;
    let thint = 5.5;
    let del = (t - tzero) / thint;
    
    let mut delt = [0.0_f32; 5];
    delt[0] = 1.0;
    delt[1] = del.sin();
    delt[2] = del.cos();
    delt[3] = (2.0 * del).sin();
    delt[4] = (2.0 * del).cos();
    
    let mut z = 0.0_f32;
    let mut gti = 0.0_f32;
    for i in 0..=lint {
        gti += bint[0][0][i as usize] * delt[i as usize];
    }
    z = gti;
    
    let colat = 90.0 - flat;
    let dfarg = (std::f64::consts::PI / 180.0) as f32;
    
    let mut cml = [0.0_f32; 7];
    let mut sml = [0.0_f32; 7];
    
    let cl = (flon * dfarg).cos();
    let sl = (flon * dfarg).sin();
    cml[1] = cl;
    sml[1] = sl;
    for n in 2..=kmax {
        let n_u = n as usize;
        sml[n_u] = sl * cml[n_u - 1] + cl * sml[n_u - 1];
        cml[n_u] = cl * cml[n_u - 1] - sl * sml[n_u - 1];
    }
    
    for n in 1..=kmax {
        let n_u = n as usize;
        for m in 0..=n {
            let m_u = m as usize;
            let ffn = fn_arr[n_u][m_u];
            let (p, _dp, _pms) = legfun(m, ffn, constp[n_u][m_u], colat);
            
            let ffn_eff = -2.0_f32;
            
            let mut bt1 = 0.0_f32;
            if n <= kint {
                let mut gti_val = 0.0_f32;
                if m == 0 {
                    for i in 0..=lint {
                        gti_val += bint[n_u][0][i as usize] * delt[i as usize];
                    }
                    bt1 = gti_val;
                } else {
                    let mut hti_val = 0.0_f32;
                    for i in 0..=lint {
                        gti_val += bint[n_u][m_u][i as usize] * delt[i as usize];
                        hti_val += bint[m_u - 1][n_u][i as usize] * delt[i as usize];
                    }
                    bt1 = gti_val * cml[m_u] + hti_val * sml[m_u];
                }
            }
            
            let bt3 = bt1;
            let bt = 0.0_f32;
            
            z -= (ffn_eff * (bt3 - bt) + bt3) * p;
        }
    }
    
    z
}

pub fn shamdb0d(rlat: f32, flon: f32, t: f32, rz: f32) -> f32 {
    let kmax: i32 = 6;
    let kt: i32 = 4;
    let kint: i32 = 6;
    let lint: i32 = 4;
    let kext: i32 = 0;
    let lext: i32 = -1;
    
    let mut bint = [[[0.0_f32; 5]; 7]; 7];
    let bext = [[[0.0_f32; 5]; 7]; 7];
    
    for n in 0..=kmax as usize {
        for m in 0..=n {
            let mut gnm = [0.0_f32; 5];
            let mut hnm = [0.0_f32; 5];
            for j in 0..=kt as usize {
                gnm[j] = GANM_B0[n][m][j] + GBNM_B0[n][m][j] * rz;
                hnm[j] = HANM_B0[n][m][j] + HBNM_B0[n][m][j] * rz;
            }
            
            if n <= kint as usize {
                let lj = lint;
                
                if m > 0 {
                    for j in 0..=lj as usize {
                        bint[n][m][j] = gnm[j];
                        bint[m - 1][n][j] = hnm[j];
                    }
                } else {
                    for j in 0..=kt as usize {
                        bint[n][0][j] = gnm[j];
                    }
                }
            }
        }
    }
    
    schnevpd_calc(rlat, flon, t, &bint, &bext, kint, lint, kext, lext, kmax, &FN_ARR, &CONST)
}

